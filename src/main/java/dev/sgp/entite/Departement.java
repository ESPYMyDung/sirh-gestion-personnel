package dev.sgp.entite;

public class Departement
{
	//attribut
	private int id;
	private String nom;
	
	//constructeur
	
	//getter
	public int getId()
	{
		return id;
	}
	
	public String getNom()
	{
		return nom;
	}
	
	//setter
	public void setId(int id)
	{
		this.id = id;
	}
	
	public void setNom(String nom)
	{
		this.nom = nom;
	}
}
